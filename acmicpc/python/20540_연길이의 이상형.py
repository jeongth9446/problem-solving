a = list(input())

for k in a:
    if k == 'I':
        print('E', end="")
    if k == 'E':
        print('I', end="")
    if k == 'S':
        print('N', end="")
    if k == 'N':
        print('S', end="")
    if k == 'F':
        print('T', end="")
    if k == 'T':
        print('F', end="")
    if k == 'J':
        print('P', end="")
    if k == 'P':
        print('J', end="")

print()
